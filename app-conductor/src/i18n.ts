const dictionaries: Record<AppLanguage, Record<TranslationKey, string>> = { ca, es, gl, eu };

export function translate(
  language: AppLanguage,
  key: TranslationKey,
  values?: Record<string, string | number>,
) {
  const template = dictionaries[language][key] ?? ca[key];
  if (!values) return template;

  return Object.entries(values).reduce(
    (message, [name, value]) => message.replaceAll(`{${name}}`, String(value)),
    template,
  );
}

export function useI18n() {
  const { preferences } = usePreferences();
  const { language } = preferences;
  const t = useCallback(
    (key: TranslationKey, values?: Record<string, string | number>) => translate(language, key, values),
    [language],
  );

  const locale: Record<AppLanguage, string> = {
    ca: 'ca-ES',
    es: 'es-ES',
    gl: 'gl-ES',
    eu: 'eu-ES',
  };

  return { language, locale: locale[language], t };
}
